const johnBMI = johnMass / johnHeight ** 2;

const isMarkHigherBMI = markBMI > johnBMI;

if (isMarkHigherBMI) {
  console.log(`Mark's BMI (${markBMI}) is higher than John's (${johnBMI})!`);
} else {
  console.log(`John's BMI (${johnBMI}) is higher than Mark's (${markBMI})!`);
}

